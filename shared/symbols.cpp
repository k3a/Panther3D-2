int wildcmp(const wchar *pszWildcard, const wchar *pszString) {
	// Written by Jack Handy - jakkhandy@hotmail.com
	const wchar *wild = pszWildcard;
	const wchar *string = pszString;
	const wchar *cp = 0, *mp = 0;

	while ((*string) && (*wild != '*')) {
		if ((*wild != *string) && (*wild != '?')) {
			return 0;
		}
		wild++;
		string++;
	}

	while (*string) {
		if (*wild == '*') {
			if (!*++wild) {
				return 1;
			}
			mp = wild;
			cp = string+1;
		} else if ((*wild == *string) || (*wild == '?')) {
			wild++;
			string++;
		} else {
			wild = mp;
			string = cp++;
		}
	}

	while (*wild == '*') {
		wild++;
	}
	return !*wild;
}

